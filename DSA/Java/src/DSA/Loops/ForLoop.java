package DSA.Loops;

public class ForLoop {
    public static void main(String[] args) {
        /*
        * SYNTAX FOR FOR-LOOP:
        *
        * for(initialization; condition; counter)
        *   {
        *       //body
        *   }
        */

        //Q. Print numbers from 1 to 5.
        for (int num = 1; num < 6; num++){
            System.out.println(num);
        }
    }
}
